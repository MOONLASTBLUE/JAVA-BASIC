package day3;

public class StringOperation {

	public static void main(String[] args) {
		System.out.println("Hello World".length());
		System.out.println("Hello, Yoon :D bye".replace("Yoon", "min")); 
		System.out.println("Hello, [[name]] bye".replace("[[name]]", "min"));
		
	/*** replace
	 *  기존에 은 문자를 원하는 문자로 바꾸고 싶을때 사용
	 *  대문자 소문자에 유의해서 사용해야함
	 *  [[name]]을 이용해서 변경하면 더욱 더 편리
	 *  자바에 내장되어있는 여러가지 문자열을 처리가능
	 */
	}
}
	
		