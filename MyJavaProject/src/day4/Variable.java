package day4;

public class Variable {

	public static void main(String[] args) {
	
		//변수의 정의
		
		int a = 1; 
		System.out.println(a);
		// Number -> int = integer(정수) ... -2,-1,0,1,2
		
		double b = 1.1;
		System.out.println(b);
		//double(실수)
		  //int b = 1.1; // 오류 int에는 정수만 올 수 있기 때문
		  // 1.1은 real number(실수)...-2.0,-1.0,0,1 ->double
		
		String c="Hello World";
		System.out.println(c);
		//String(문자열 변수)
	}

}
